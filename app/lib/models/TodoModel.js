import mongoose from "mongoose";

const todos = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    isCompleted: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);
const TodoModel = mongoose.models.Tasks || mongoose.model("Tasks", todos);

export default TodoModel;
